    def load_cartridge(self, rom_data):
        self.rom = rom_data
        # Load ROM into memory bank 0
        for i in range(min(len(rom_data), 0x8000)):
            self.memory[i] = rom_data[i]
    
    def read_byte(self, address):
        address &= 0xFFFF
        
        # Boot ROM override
        if self.boot_rom_enabled and address < 0x100:
            return self.boot_rom[address]
        
        # ROM banks
        if address < 0x8000:
            return self.memory[address]
        
        # Video RAM
        elif address >= 0x8000 and address < 0xA000:
            return self.memory[address]
        
        # External RAM
        elif address >= 0xA000 and address < 0xC000:
            return self.memory[address]
        
        # Work RAM
        elif address >= 0xC000 and address < 0xE000:
            return self.memory[address]
        
        # Echo RAM
        elif address >= 0xE000 and address < 0xFE00:
            return self.memory[address - 0x2000]
        
        # OAM
        elif address >= 0xFE00 and address < 0xFEA0:
            return self.memory[address]
        
        # Unusable
        elif address >= 0xFEA0 and address < 0xFF00:
            return 0xFF
        
        # I/O registers
        elif address >= 0xFF00 and address < 0xFF80:
            return self.memory[address]
        
        # High RAM
        elif address >= 0xFF80 and address < 0xFFFF:
            return self.memory[address]
        
        # Interrupt Enable Register
        elif address == 0xFFFF:
            return self.memory[address]
        
        return 0xFF
    
    def write_byte(self, address, value):
        address &= 0xFFFF
        value &= 0xFF
        
        # Boot ROM disable
        if address == 0xFF50:
            self.boot_rom_enabled = False
        
        # ROM banks - not writable
        if address < 0x8000:
            return
        
        # Video RAM
        elif address >= 0x8000 and address < 0xA000:
            self.memory[address] = value
        
        # External RAM
        elif address >= 0xA000 and address < 0xC000:
            self.memory[address] = value
        
        # Work RAM
        elif address >= 0xC000 and address < 0xE000:
            self.memory[address] = value
        
        # Echo RAM
        elif address >= 0xE000 and address < 0xFE00:
            self.memory[address - 0x2000] = value
        
        # OAM
        elif address >= 0xFE00 and address < 0xFEA0:
            self.memory[address] = value
        
        # Unusable
        elif address >= 0xFEA0 and address < 0xFF00:
            return
        
        # I/O registers
        elif address >= 0xFF00 and address < 0xFF80:
            self.memory[address] = value
        
        # High RAM
        elif address >= 0xFF80 and address < 0xFFFF:
            self.memory[address] = value
        
        # Interrupt Enable Register
        elif address == 0xFFFF:
            self.memory[address] = value
    
    def read_word(self, address):
        return self.read_byte(address) | (self.read_byte(address + 1) << 8)
    
    def write_word(self, address, value):
        self.write_byte(address, value & 0xFF)
        self.write_byte(address + 1, (value >> 8) & 0xFF)
